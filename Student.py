class Student:

    def __init__ (self,ID,preferences,alloted,CG):
        self.ID=ID
        self.preferences=preferences
        self.alloted=alloted
        self.CG=CG

    def allotCourse(alloted):
    	self.alloted=alloted

    def getAllotedCourse():
    	return self.alloted

    def getPreferences():
    	return self.preferences

    def getCG():
    	return self.CG


